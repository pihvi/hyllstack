const typeDefs = gql`
  type Book {
    title: String!
    published: Int!
    author: String!
    genres: [String!]!
    id: ID!
  }

  type Author {
    name: String!
    born: Int
    id: ID!
    bookCount: Int!
  }

  type Query {
    hello: String!
    bookCount: Int!
    authorCount: Int!
    allBooks(author: String, genre: String): [Book!]!
    allAuthors: [Author!]!
  }

  type Mutation {
    addBook(
      title: String!
      author: String!
      published: Int!
      genres: [String!]!
    ): Book

    editAuthor(
      name: String!
      setBornTo: Int!
    ): Author
  }
`

const resolvers = {
  Mutation: {
    editAuthor: (root, args) => {
      const author = authors.find(x => x.name === args.name)
      if (author) {
        author.born = args.setBornTo
      }
      return author
    },
    addBook: (root, args) => {
      const book = {...args, id: uuidv1()}
      books = books.concat(book)
      if (authors.find(x => x.name === book.author)) {
        return book
      } else {
        authors = authors.concat({name: book.author, id: uuidv1()})
        return book
      }
    }
  },
  Query: {
    hello: () => 'world',
    bookCount: () => books.length,
    authorCount: () => authors.length,
    allBooks: (root, args) => {
      let res = books
      if (args.author) {
        res = res.filter(x => x.author === args.author)
      }
      if (args.genre) {
        res = res.filter(x => x.genres.includes(args.genre))
      }
      return res
    },
    allAuthors: () => authors
      .map(author => ({
        ...author, bookCount: books.filter(x => x.author === author.name).length
      })),
  }
}

const server = new ApolloServer({
  typeDefs,
  resolvers,
})

server.listen().then(({ url }) => {
  console.log(`Server ready at ${url}`)
})
